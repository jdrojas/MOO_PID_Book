function [Kc,Ti,Td]=Jrd3Deg_PID1tuning(K,T,L,Deg)
%
nin=nargin;
if (nin==3)
	Deg=0;
end;
%
switch Deg
    case 0.25
        a1=0.8787;b1=-0.9280;c1= 0.2033;
        a2=0.8154;b2= 0.6431;c2= 0.4502;
        a3=0.2794;b3= 0.8765;c3=-0.0149;
    case 0.35
        a1=0.7490;b1=-0.9348;c1= 0.2669;
        a2=0.8664;b2= 0.6033;c2= 0.3874;
        a3=0.2757;b3= 0.8698;c3=-0.0070;
    case 0.45
        a1=0.6292;b1=-0.9444;c1= 0.3195;
        a2=0.8871;b2= 0.5847;c2= 0.3466;
        a3=0.2804;b3= 0.8471;c3=-0.0037;
    case 0.55
        a1=0.5252;b1=-0.9492;c1= 0.3494;
        a2=0.8755;b2= 0.5830;c2= 0.3275;
        a3=0.2949;b3= 0.8123;c3=-0.0055;
    otherwise
        a1=1.1410;b1=-0.9664;c1= 0.1468;
        a2=1.0860;b2= 0.4896;c2= 0.2775;
        a3=0.3726;b3= 0.7098;c3=-0.0409;
end
%
%Parameters of the PID
Kc=1/K*(a1*(L/T)^b1+c1);
Ti=T*(a2*(L/T)^b2+c2);
Td=T*(a3*(L/T)^b3+c3);
%